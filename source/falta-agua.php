        <h2>Falta de água</h2>
      <p> A falta de água é uma ameaça, uma vez que a água é fonte de vida. Estamos tão habituados nça da água que só damos conta da sua importância quando ela nos faz falta, mas cisa mudar.  </p>
        <p> Preservar os recursos hídricos é preservar a nossa existência. Para se ter uma ideia, a maior parte das células do nosso corpo possui água, por isso ingerir água é uma necessidade do corpo humano.</p>
        <p> Muitos países no mundo sofrem com a escassez ou má distribuição de água, tais como países do Oriente Médio, sul da Ásia e o norte da África. Já outros países da América, onde há maior distribuição, sofrem com a contaminação. Infelizmente, muitos dos problemas que evolvem a contaminação da água são de responsabilidade das indústrias e da própria população.</p>
        <p> O crescimento desordenado das cidades faz com que muitos bairros sejam construídos próximos a locais onde têm rios e lagos, o que contribui para que as pessoas, inconscientes da importância da água, descartem o lixo de forma imprópria.</p>
        <p> O ideal seria que toda a água utilizada fosse encaminhada para as redes de tratamento de esgoto, porém nem sempre isso é o que acontece. Nas grandes cidades, por exemplo, é possível ver muitos esgotos a céu aberto sem nenhuma probabilidade de reaproveitamento.</p>
        <p> Ainda é possível ver rios e mares poluídos, prejudicando o habitat natural de muitas espécies de animais, sobretudo os peixes, e, consequentemente, o sustento de quem depende da pesca para sobreviver.</p>
        <p> No Brasil, rios como o Madeira e o Cuiabá, localizados na região Amazônica, apresentam contaminação por minérios (metal e mercúrio) provenientes de garimpo clandestino e agrotóxicos de lavouras.</p>
        <p> Diante disso, o poder público e a população precisam se conscientizar sobre a importância da água e juntos elaborar estratégias para combater os problemas ambientais que ameaçam a disponibilidade desse recurso no planeta.</p>
