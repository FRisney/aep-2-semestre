        <h2>Dicas para economia de água em Curitiba</h2>
        <h3>Diminuir o tempo de banho:</h3>
        <p>Considerando uma vazão média de 6 litros de água por minuto, um banho de 10 minutos consome 60 litros de água. Diminua esse tempo para cinco minutos e economize 30 litros. Em uma casa com quatro pessoas, serão economizados 120 litros por dia. No final do mês, a economia será de 3.600 litros de água somente reduzindo pela metade o tempo do banho.</p>
        <h3>Manter a torneira fechada para escovar os dentes ou usar um copo para enxágue:</h3>
        <p>Mantenha a torneira fechada ao escovar os dentes. Em apenas dois minutos de torneira aberta, você gasta cerca de 13,5 litros de água. Se você abrir o mínimo torneira, consumindo, por exemplo, 3,5 litros, serão preservados 10 litros de água. Ao escovar os dentes três vezes ao dia, serão 30 litros economizados. Em um mês, uma única pessoa, terá economizado 900 litros de água somente controlando a torneira na escovação.</p>
        <h4>Não faça a barba durante o banho.</h4>
        <h3>Descarna no vaso sanitário:</h3>
        <p>As caixas de descarga acopladas ao vaso sanitário têm vazão de 6 litros por vez. Coloque dentro das caixas uma garrafa de refrigerante embalagem PET de 1 litro com terra ou pedra ou água da máquina de lavar. Isso economizará um litro por descarga sem comprometer a eficiência. Considerando que uma pessoa utiliza o vaso sanitário em média quatro vezes ao dia, em uma casa com quatro pessoas serão economizados 16 litros de água. Em um mês: 480 litros de economia.</p>
        <h3>Lavar louça:</h3>
        <p> O ideal é acumular a louça. Por exemplo, empilhe e separe a louça do café e lave-a junto com a louça do almoço. Em média, uma torneira de pia de cozinha, funcionando 15 minutos consome 120 litros de água. Ensaboe a louça com a torneira fechada. Se a economia for de 60 litros, em 30 dias serão 1,8 mil litros.</p>
        <h3>Vazamentos:</h3>
        <p>Preste atenção e conserte eventuais vazamentos na sua casa. Um buraco de 2 mm em um cano de uma única casa pode desperdiçar 3.200 litros de água por mês.</p>
        <h3>Lavar Carros e Calçadas:</h3>
        <p>Use a vassoura para limpar o quintal, a calçada ou as áreas comuns de prédios e empresas ou reutilize a água que sai da máquina de lavar roupa.</p>
        <p>Para se ter uma ideia, uma mangueira ligada por 15 minutos gasta 280 litros de água.  Use um balde e um pano para limpar o carro. Lembre-se novamente: uma mangueira ligada por 15 minutos gasta 280 litros de água. O carro nem pensar em lavar.</p>
        <h3>Redutor de Água:</h3>
        <p>Uma peça de fácil instalação e que pode diminuir pela metade a quantidade de água que sai das torneiras. Conhecida nas lojas de material de construção como “redutor de vazão”, pode ser encontrada em diferentes modelos e kits.</p>
        <p>Os redutores são pequenos anéis que controlam a quantidade de água na saída das torneiras de banheiros, cozinhas e tanques. A instalação é simples e não requer contratação de encanador.</p>
        <p>Considerando o consumo médio da torneira de pia de lavar louça, com 120 litros em 15 minutos, o uso do redutor pode trazer uma economia de, no mínimo, 50%. Ou seja, 1,8 mil litros em um mês</p>
